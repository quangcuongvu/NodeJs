/*
IP
- mỗi 1 máy chủ được gọi là 1 HOST
- IP là từ tiếng anh viết tắt của Internet Protocol, được dịch là giao thức kết nối 
    internet, là địa chỉ để các thiết bị ngày nay nhận diện và liên lạc (kết nối) với nhau trên mạng internet.

- Địa chỉ IP là duy nhất

PORT
- mỗi 1 máy chủ được gọi là 1 HOST
- 1 HOST có thể có nhiều ứng dụng mạng chờ kết nối.
- Với 1 địa chỉ IP, chúng ta chỉ có thể xác định được một máy tính duy nhất trên mạng, tuy nhiên khi 1 máy tính chạy nhiều
    dịch vụ khác nhau thì chúng ta phải được phân biệt bởi khái niệm PORT


*/